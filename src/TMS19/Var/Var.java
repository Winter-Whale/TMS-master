package TMS19.Var;

public class Var {
    public static void main(String[] args) {
        System.out.println("Примеры использования VAR в коде");
        var number = 15;
        var message = "Hello World";
        var output = "A";
        System.out.println("Объявление int через var: " + number);
        System.out.println("Объявление string через var: " + message);
        System.out.println("Объявление char через var: " + output);
    }
}
